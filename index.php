			<div class="container text-center">
				<h3>Brief History</h3>
				<p>BNHOC is a legacy of the persistent advancement of BN hydrography. Thus its history is necessarily integrated with the history of BN Hydrographic Department which started it’s journey by establishing Hydrographic School in 1983 at BNS ISSA KHAN. Having felt the necessity to manage and organize the distribution of charts and publications, BN Chart Depot was established in the same year.</p>
				<p>BN Hydrographic Department underwent outstanding transformation to the much needed digital hydrographic surveying capabilities with the help of French Government under the scheme of Hydro Bangla Project-1 in 1996. The next major step of modernizing was Hydro Bangla Project-2, which was conducted in 2001. Under both the projects, a good number of officers and sailors were trained both in France and in Bangladesh by French surveyors. After those projects, BN shifted from analogue/conventional survey to digital survey. Those projects were the milestones to bring the BN Hydrographic Department to the international standard.</p>
				<p>After modernization of hydrography department the responsibility and sector of hydrographic survey increased in manifold. But BN Hydrographic Department didn’t have a full-fledged organization to monitor the survey works, data processing, cartographic works, storing of data, circulation and in general a research institute. Necessity was felt to establish a centre as the hub of all hydrographic activities of BN. In order to perform all those tasks BN Hydrographic and Oceanographic Centre (BNHOC) was established in the year of 2001 at the end of Hydro Bangla Project-2 and BN Chart Depot was merged with that centre. Establishment of this organization has given a new dimension and a wide horizon to the survey department. With the enhanced capability of international standard hydrographic survey, BN brought home the prestigious membership of International Hydrographic Organization (IHO) in 2001. Subsequently we were entrusted by IHO with nine international charts.</p>

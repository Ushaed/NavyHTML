			<div class="container text-center">
				<h3>Mission</h3>
				<p>Being a littoral state of Bay of Bengal, sea is of great importance to Bangladesh. 94% of her trade and commerce are seaborne. On the other hand, exploration and exploitation of living and non-living resources also need various kinds of technical data of the sea. Therefore, Government of Peoples Republic of Bangladesh has handed over the responsibility of hydrographic survey and oceanographic data collection at Bay of Bengal and coastal areas to Bangladesh Navy. Hence the mission of BNHOC is:</p>
				<p><strong>To ensure availability of reliable and updated information of the marine environment in order to derive maximum benefits from Bay of Bengal.</strong></p>
				<p>To fulfill its objectives, BNHOC supports national infrastructure through its contribution to:Efficient and Safe Maritime Transportation</p>
				<ul>
					<li>Coastal Zone Management</li>
					<li>Maritime Boundary Delineation</li>
					<li>Exploration and Exploitation of Marine Resources</li>
					<li>Environmental Protection and Management</li>
					<li>Naval Warfare</li>
					<li>Environmental protection</li>
					<li>Pre & Post Dredging</li>
					<li>Marine Scientific Research</li>
					<li>Salvage Operation</li>
				</ul>
				<h4>Role</h4>
				<p>The role of the BNHOC is production, procurement and distribution of navigational charts and publications to BN ships and establishments as well as maintenance of hydrographic, oceanographic, cartographic, meteorological and data processing instrument/equipment.</p>
